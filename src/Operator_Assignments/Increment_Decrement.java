package Operator_Assignments;

public class Increment_Decrement {
    public static void main(String[] args) {
        int x=10;
        char ch = 'a';
        double d = 10.5;
        int y=++x;
        ch++;
        --d;
         x++;
         y--;
        System.out.println(y);
        System.out.println(x);
        System.out.println(ch);
        System.out.println(d);
    }
}
